// Validation function
export const isValidCollegeEmail = (email: string): boolean => {
  const lowercaseEmail = email.toLowerCase();

  // Check against all college domains
  return COLLEGE_DOMAINS.some(domain => lowercaseEmail.includes(domain.toLowerCase()));
};

// Backward compatibility
export const isValidOdishaCollegeEmail = isValidCollegeEmail;

